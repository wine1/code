/**
 * @param {character[][]} board
 * @return {boolean}
 */
var isValidSudoku = function(board) {
    let rows = {};
    let columns = {};
    let boxs = {};
  
    for (let i = 0; i < 9; i++) {
      for (let j = 0; j < 9; j++) {
        let num = board[i][j];
        if (num != ".") {
          //   boxIndex为3*3九宫格的序号
          let boxIndex = parseInt(i / 3) * 3 + parseInt(j / 3);
          if (
            rows[i + "-" + num] ||
            columns[j + "-" + num] ||
            boxs[boxIndex + "-" + num]
          ) {
            return false;
          }
          rows[i + "-" + num] = true;
          columns[j + "-" + num] = true;
          boxs[boxIndex + "-" + num] = true;
        }
      }
    }
    return true;
  };
